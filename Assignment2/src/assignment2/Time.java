package assignment2;

public class Time {
    String key;
    String day;
    String day_of_the_week;
    String month;
    String quarter;
    String year;
    
    public Time(String k, String d, String w, String m, String q, String y) {
        key = k;
        day = d;
        day_of_the_week = w;
        month = m;
        quarter = q;
        year = y;
    }
}
